function contador1(evt){
    var compro = document.getElementById("conectar").value;
	if (compro == "no"){
	alert("Debe iniciar sesión para realizar esta acción");
	evt.preventDefault();
	}
	else
	{
	
	var okscont = parseInt(document.getElementById("oks1").innerHTML); 
	var valhidden = parseInt(document.getElementById("text1").value);
	var resultado = 0; 
	if ((valhidden == -1) && (okscont == 0))
	{ 
		valhidden = 1; 
	}
	resultado = okscont + valhidden; 
	document.getElementById("oks1").innerHTML = resultado;  
	document.getElementById("datos1").value = resultado; 
	if (valhidden == 1) 
	{
		document.getElementById("text1").value=-1;
	}
	else
	{
		document.getElementById("text1").value=1;
	}
	}
}

